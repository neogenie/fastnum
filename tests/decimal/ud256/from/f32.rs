#[rstest]
#[trace]
#[case(0.0, udec256!(0))]
#[case(1234f32, udec256!(1234))]
#[case(1.0, udec256!(1.0))]
#[case(2.0, udec256!(2.0))]
#[case(3.0, udec256!(3.0))]
#[case(0.5, udec256!(0.5))]
#[case(0.25, udec256!(0.25))]
#[case(50000., udec256!(50000))]
#[case(0.1, udec256!(0.100000001490116119384765625))]
#[case(0.01, udec256!(0.00999999977648258209228515625))]
#[case(0.001, udec256!(0.001000000047497451305389404296875))]
#[case(12.34, udec256!(12.340000152587890625))]
#[case(0.3333333, udec256!(0.333333313465118408203125))]
#[case(5.0 * 0.03125, udec256!(0.15625))]
#[case(1.0 / 3.0, udec256!(0.3333333432674407958984375))]
#[case(PI, udec256!(3.1415927410125732421875))]
#[case(PI * 10000.0f32, udec256!(31415.927734375))]
#[case(PI * 30000.0f32, udec256!(94247.78125))]
#[case(f32::EPSILON, udec256!(1.1920928955078125E-7))]
#[case(3.0000000000000004, udec256!(3))]
#[case(50., udec256!(50))]
#[case(f32::MAX, udec256!(340282346638528859811704183484516925440))]
fn test_from_f32_ok(#[case] n: f32, #[case] expected: UD256) {
    let d = UD256::try_from(n).unwrap();
    assert_eq!(d, expected);

    let n = f32::from_bits(n.to_bits() | (1 << 31));
    let r = UD256::try_from(n);
    assert!(r.is_err());
}

