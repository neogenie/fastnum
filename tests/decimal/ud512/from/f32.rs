use std::f32::consts::PI;

use rstest::*;

use fastnum::{udec512, UD512};

#[rstest]
#[trace]
#[case(f32::MIN_POSITIVE, udec512!(1.1754943508222875079687365372222456778186655567720875215087517062784172594547271728515625e-38))]
#[case(317e-40, udec512!(3.1700000098946435501119816090716154772221806896649747100732700841687651538425285480116144753992557525634765625E-38))]
#[case(2.35098744048e-38, udec512!(2.350987440475957123602109243087866394712812961308427354153308831195379018097479928428583662025630474090576171875E-38))]
#[case(2.3509889819e-38, udec512!(2.35098898190426788090088725919040801362055736959656341832065776397049129686767088287524529732763767242431640625E-38))]
fn test_from_f32_ok(#[case] n: f32, #[case] expected: UD512) {
    let d = UD512::try_from(n).unwrap();
    assert_eq!(d, expected);

    let n = f32::from_bits(n.to_bits() | (1 << 31));
    let r = UD512::try_from(n);
    assert!(r.is_err());
}

